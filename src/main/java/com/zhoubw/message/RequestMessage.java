package com.zhoubw.message;

public class RequestMessage {
    private String name;
    private String toname;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getToname() {
        return toname;
    }

    public void setToname(String toname) {
        this.toname = toname;
    }
}
